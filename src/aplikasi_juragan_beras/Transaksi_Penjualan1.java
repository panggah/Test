    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        String no=notransaksi.getText();
        String nopel=nopelanggan.getText();
        String namatok=namatoko.getText();
        String namapel=namapelanggan.getText();
        String tgltran=((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
        String jnis=jenisbarang.getSelectedItem().toString();
        String jumlahbel=jumlahbeli.getText();
        String hrg=harga.getText();
        String tot=totalharga.getText();
        String bayar=bayar2.getText();
        String kmbl=kembali.getText();
        
        if((no.equals(""))||(nopel.equals(""))||(namatok.equals(""))||(namapel.equals(""))
                ||(tgltran.equals(""))||(jnis.equals(""))||(jumlahbel.equals(""))
                ||(hrg.equals(""))||(tot.equals(""))
                ||(bayar.equals(""))||(kmbl.equals(""))){
                JOptionPane.showMessageDialog(rootPane, "Data Ada Yang Kosong", "INFORMASI", WIDTH, null);
                notransaksi.requestFocus();
                
            }
            else {
         boolean j=k.simpanData("UPDATE `tbl_jual` SET `No` ='"+no+"'," +
                                "`No. Pelanggan` = '"+nopel+"'," +
                                "`Nama Toko` = '"+namatok+"'," +
                                "`Nama Pelanggan` = '"+namapel+"'," +
                                "`Tanggal Transaksi` = '"+tgltran+"'," +
                                "`Jenis Beras` = '"+jnis+"'," +
                                "`Jumlah Beli` = '"+jumlahbel+"'," +
                                "`Harga` = '"+hrg+"'," +
                                "`Total` = '"+tot+"'," +
                                "`Bayar` = '"+bayar+"'," +
                                "`Kembali` = '"+kmbl+"'" +
                                "WHERE `No` = '"+no+"'" +
                                "AND `No. Pelanggan` = '"+nopel+"'");
        JOptionPane.showMessageDialog(rootPane, "Berhasil di Update");     
        tampilkandata();
        }    // TODO add your handling code here:
    }//GEN-LAST:event_btnEditActionPerformed


