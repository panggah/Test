    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String nopl=nopelanggan.getText();
        DefaultTableModel tbjual = new DefaultTableModel();
       
        tbjual.addColumn("No. Pelanggan");
        tbjual.addColumn("Nama Toko");
        tbjual.addColumn("Nama Pelanggan");
        
        if(nopl.equals("")){
            JOptionPane.showMessageDialog(rootPane, "NO PELANGGAN HARUS DI ISI", "INFORMASI", WIDTH, null);
                nopelanggan.requestFocus();
        }
        else{
         try {
            rs=k.ambilData("Select * from tbl_pelanggan where No='"+nopl+"'");
             
            if (rs.next()) {
        
        namatoko.setText(rs.getString(3));
        namapelanggan.setText(rs.getString(2));
        nopelanggan.setEnabled(false);
        namatoko.setEnabled(false);
        namapelanggan.setEnabled(false);
        jDateChooser1.requestFocus();
            }          
            else{
                JOptionPane.showMessageDialog(rootPane, "Data Tidak Ditemukan");
                nopelanggan.requestFocus();
            }
           } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Error Pengambilan Data ");
        }
        }    // TODO add your handling code here:
    }//GEN-LAST:event_jButton8ActionPerformed

